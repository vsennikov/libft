#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*res;

	i = ft_strlen((char *) s) + 1;
	res = (char *)malloc(i * sizeof(char));
	if (res == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
